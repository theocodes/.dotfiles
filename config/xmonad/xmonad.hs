main = do
  h <- spawnPipe "xmobar"
  xmonad $ docks def {
      -- simple stuff
        terminal           = myTerminal,
        focusFollowsMouse  = myFocusFollowsMouse,
        clickJustFocuses   = myClickJustFocuses,
        borderWidth        = myBorderWidth,
        modMask            = myModMask,
        workspaces         = myWorkspaces,
        normalBorderColor  = myNormalBorderColor,
        focusedBorderColor = myFocusedBorderColor,

      -- key bindings
        keys               = myKeys,
        mouseBindings      = myMouseBindings,

      -- hooks, layouts
        layoutHook         = myLayout,
        manageHook         = myManageHook,
        handleEventHook    = myEventHook,
        logHook            = dynamicLogWithPP $ def { ppOutput = hPutStrLn h
                                                    , ppCurrent = xmobarColor "#50fa7b" "" . wrap "" "" -- Current workspace
                                                    , ppVisible = xmobarColor "#bd93f9" ""                -- Visible but not current workspace
                                                    , ppHidden = xmobarColor "#44475a" "" . wrap "" ""   -- Hidden workspaces
                                                    , ppTitle = xmobarColor "#bd93f9" "" . shorten 60               -- Title of active window
                                                    , ppSep =  "<fc=#44475a> <fn=1>|</fn> </fc>"                    -- Separator character
                                                    , ppUrgent = xmobarColor "#C45500" "" . wrap "!" "!"            -- Urgent workspace
                                                    , ppOrder  = \(ws:l:t:ex) -> [ws,l]++ex++[t]                    -- order of things in xmobar
                                                    },
        startupHook        = myStartupHook
    }

-- default config

